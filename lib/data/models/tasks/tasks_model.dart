class TaskModel {
  final int id;
  final String title;
  final String date;
  final String time;
  final String description;
  final String category;
  final int color;
  final String status;

  TaskModel({
    required this.id,
    required this.title,
    required this.date,
    required this.time,
    required this.description,
    required this.category,
    required this.color,
    required this.status,
  });

}
